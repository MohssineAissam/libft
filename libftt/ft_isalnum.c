#include "libft.h"

int	ft_isalnum(int s)
{
	if (((s >= 'a' && s <= 'z') || (s >= 'A' && s <= 'Z'))
		|| (s >= 48 && s <= 57))
	{
		return (1);
	}
	return (0);
}
